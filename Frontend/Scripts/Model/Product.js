class Product {
        constructor(id, productnr, productname, expirationdate, stock, category, price) {
            this.id = id;
            this.productnr = productnr;
            this.productname = productname;
            this.expirationdate = expirationdate;
            this.stock = stock;
            this.category = category;
            this.price = price;
        }
}
